public class Prog122i {
    public static void main(String[] args) {
        System.out.println("Number\tCube\tCube Root");
        double lcv = -25;
        while (lcv <= 25) {
            double cube = lcv * lcv * lcv;
            double cubeRoot;
            if (lcv >= 0){
                cubeRoot = Math.pow(lcv, ((double)1/3));
            } else {
                cubeRoot = Math.pow(((-1 ) * lcv), ((double)1/3));
                cubeRoot *= 1;
            }

            System.out.printf("%f\t%f\t%.4f\n", lcv, cube, cubeRoot);
            lcv++;

        }
    }
}
